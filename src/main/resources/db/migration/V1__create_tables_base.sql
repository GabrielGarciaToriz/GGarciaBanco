CREATE TABLE USUARIO (
    ID_USUARIO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PUBLIC_ID VARCHAR2(36) NOT NULL,

    NOMBRE VARCHAR2(100) NOT NULL,
    APELLIDO_PATERNO VARCHAR2(100) NOT NULL,
    APELLIDO_MATERNO VARCHAR2(100),

    CORREO VARCHAR2(150) NOT NULL,

    PASSWORD_HASH VARCHAR2(255) NOT NULL,

    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,
    FECHA_REGISTRO TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT UQ_USUARIO_PUBLIC_ID UNIQUE (PUBLIC_ID),
    CONSTRAINT UQ_USUARIO_CORREO UNIQUE (CORREO),
    CONSTRAINT CK_USUARIO_ACTIVO CHECK (ACTIVO IN (0, 1))
);

CREATE TABLE BANCO (
    ID_BANCO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    NOMBRE_BANCO VARCHAR2(150) NOT NULL,

    ES_PREDETERMINADO NUMBER(1) DEFAULT 0 NOT NULL,
    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,

    CONSTRAINT UQ_BANCO_NOMBRE UNIQUE (NOMBRE_BANCO),
    CONSTRAINT CK_BANCO_DEFAULT CHECK (ES_PREDETERMINADO IN (0, 1)),
    CONSTRAINT CK_BANCO_ACTIVO CHECK (ACTIVO IN (0, 1))
);

CREATE UNIQUE INDEX UQ_BANCO_PREDETERMINADO
ON BANCO (
    CASE
        WHEN ES_PREDETERMINADO = 1 THEN ES_PREDETERMINADO
    END
);

CREATE TABLE TARJETA (
    ID_TARJETA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    ID_USUARIO NUMBER NOT NULL,
    ID_BANCO NUMBER NOT NULL,

    NUMERO_TARJETA VARCHAR2(19) NOT NULL,

    ACTIVA NUMBER(1) DEFAULT 1 NOT NULL,
    FECHA_REGISTRO TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT FK_TARJETA_USUARIO
        FOREIGN KEY (ID_USUARIO)
        REFERENCES USUARIO(ID_USUARIO),

    CONSTRAINT FK_TARJETA_BANCO
        FOREIGN KEY (ID_BANCO)
        REFERENCES BANCO(ID_BANCO),

    CONSTRAINT UQ_TARJETA_NUMERO
        UNIQUE (NUMERO_TARJETA),

    CONSTRAINT CK_TARJETA_ACTIVA
        CHECK (ACTIVA IN (0, 1)),

    CONSTRAINT CK_TARJETA_NUMERO_FORMATO
        CHECK (REGEXP_LIKE(NUMERO_TARJETA, '^[0-9]{13,19}$'))
);

CREATE TABLE CAJERO (
    ID_CAJERO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    NOMBRE VARCHAR2(100) NOT NULL,
    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,

    CONSTRAINT UQ_CAJERO_NOMBRE UNIQUE (NOMBRE),
    CONSTRAINT CK_CAJERO_ACTIVO CHECK (ACTIVO IN (0, 1))
);

CREATE TABLE DENOMINACION (
    ID_DENOMINACION NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    TIPO VARCHAR2(10) NOT NULL,
    VALOR_CENTAVOS NUMBER(10, 0) NOT NULL,
    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,

    CONSTRAINT CK_DENOMINACION_TIPO
        CHECK (TIPO IN ('BILLETE', 'MONEDA')),

    CONSTRAINT CK_DENOMINACION_VALOR
        CHECK (VALOR_CENTAVOS > 0),

    CONSTRAINT CK_DENOMINACION_ACTIVO
        CHECK (ACTIVO IN (0, 1)),

    CONSTRAINT UQ_DENOMINACION_VALOR
        UNIQUE (VALOR_CENTAVOS)
);

CREATE TABLE CAJERO_INVENTARIO (
    ID_CAJERO NUMBER NOT NULL,
    ID_DENOMINACION NUMBER NOT NULL,
    CANTIDAD_ACTUAL NUMBER(10, 0) NOT NULL,

    CONSTRAINT PK_CAJERO_INVENTARIO
        PRIMARY KEY (ID_CAJERO, ID_DENOMINACION),

    CONSTRAINT FK_INV_CAJERO
        FOREIGN KEY (ID_CAJERO)
        REFERENCES CAJERO(ID_CAJERO),

    CONSTRAINT FK_INV_DENOMINACION
        FOREIGN KEY (ID_DENOMINACION)
        REFERENCES DENOMINACION(ID_DENOMINACION),

    CONSTRAINT CK_INV_CANTIDAD
        CHECK (CANTIDAD_ACTUAL >= 0)
);

CREATE TABLE RETIRO (
    ID_RETIRO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    ID_CAJERO NUMBER NOT NULL,
    ID_TARJETA NUMBER NOT NULL,

    MONTO_CENTAVOS NUMBER(12, 0) NOT NULL,
    FECHA TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    ESTADO VARCHAR2(20) NOT NULL,
    MENSAJE VARCHAR2(255),

    CONSTRAINT FK_RETIRO_CAJERO
        FOREIGN KEY (ID_CAJERO)
        REFERENCES CAJERO(ID_CAJERO),

    CONSTRAINT FK_RETIRO_TARJETA
        FOREIGN KEY (ID_TARJETA)
        REFERENCES TARJETA(ID_TARJETA),

    CONSTRAINT CK_RETIRO_MONTO
        CHECK (MONTO_CENTAVOS > 0),

    CONSTRAINT CK_RETIRO_ESTADO
        CHECK (ESTADO IN ('APROBADO', 'RECHAZADO'))
);

CREATE TABLE RETIRO_DETALLE (
    ID_RETIRO NUMBER NOT NULL,
    ID_DENOMINACION NUMBER NOT NULL,

    CANTIDAD NUMBER(10, 0) NOT NULL,
    SUBTOTAL_CENTAVOS NUMBER(12, 0) NOT NULL,

    CONSTRAINT PK_RETIRO_DETALLE
        PRIMARY KEY (ID_RETIRO, ID_DENOMINACION),

    CONSTRAINT FK_DET_RETIRO
        FOREIGN KEY (ID_RETIRO)
        REFERENCES RETIRO(ID_RETIRO),

    CONSTRAINT FK_DET_DENOMINACION
        FOREIGN KEY (ID_DENOMINACION)
        REFERENCES DENOMINACION(ID_DENOMINACION),

    CONSTRAINT CK_DET_CANTIDAD
        CHECK (CANTIDAD > 0),

    CONSTRAINT CK_DET_SUBTOTAL
        CHECK (SUBTOTAL_CENTAVOS > 0)
);